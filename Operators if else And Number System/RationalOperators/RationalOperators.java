import java.lang.*;

public class RationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("a = " + a + ", b = " + b);

        // == operator
        System.out.println("a == b: " + (a == b)); // false

        // != operator
        System.out.println("a != b: " + (a != b)); // true

        // > operator
        System.out.println("a > b: " + (a > b)); // false

        // < operator
        System.out.println("a < b: " + (a < b)); // true

        // >= operator
        System.out.println("a >= b: " + (a >= b)); // false

        // <= operator
        System.out.println("a <= b: " + (a <= b)); // true
    }
}
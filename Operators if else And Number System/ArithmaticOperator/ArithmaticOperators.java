public class ArithmaticOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 25;
        System.out.println((int) a + b);
        System.out.println((int) b - a);
        System.out.println((int) a * b);
        System.out.println((int) b / a);
        System.out.println(c % b);
        double x = 10.5;
        double y = 20.5;
        System.out.println(x + y);
        System.out.println(y - x);
        System.out.println(x * y);
        System.out.println(y / x);
        System.out.println(x % y);

    }
}